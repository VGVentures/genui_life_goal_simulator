abstract class Spacing {
  static const double spaceUnit = 4;

  /// xxs spacing value (4pt)
  static const double xxs = spaceUnit;

  /// xs spacing value (8pt)
  static const double xs = spaceUnit * 2;

  /// sm spacing value (12pt)
  static const double sm = spaceUnit * 3;

  /// md spacing value (16pt)
  static const double md = spaceUnit * 4;

  /// lg spacing value (20pt)
  static const double lg = spaceUnit * 5;

  /// xl spacing value (24pt)
  static const double xl = spaceUnit * 6;

  /// xxl spacing value (28pt)
  static const double xxl = spaceUnit * 7;

  /// xxxl spacing value (32pt)
  static const double xxxl = spaceUnit * 8;
}
